const calculator = {
  add: function (numOne, numTwo) {
    return numOne + numTwo;
  },
  subtract: function (numOne, numTwo) {
    return numOne - numTwo;
  },
  divide: function (numOne, numTwo) {
    return numOne / numTwo;
  },
  multiply: function (numOne, numTwo) {
    return numOne * numTwo;
  },
};

module.exports = calculator;
